# Create on module named as Arithmetic which contains 4 functions
# as Add() for addition, Sub() for subtraction, Mult() for
# multiplication and Div() for division
# ================================================================

# ================================
# Add : addition of given numbers
# ================================
def Add(iNum1, iNum2):

    iSum = 0

    # Perform calculation
    iSum = iNum1 + iNum2

    return iSum

# ================================
# Sub : substraction of given numbers
# ================================
def Sub(iNum1, iNum2):

    iSum = 0

    # Perform calculation
    iSum = iNum1 - iNum2

    return iSum

# ================================
# Mult : multiplicatin of given numbers
# ================================
def Mult(iNum1, iNum2):

    iSum = 0

    # Perform calculation
    iSum = iNum1 * iNum2

    return iSum

# ================================
# Div : division of given numbers
# ================================
def Div(iNum1, iNum2):

    iSum = 0

    # Perform calculation
    iSum = iNum1 / iNum2

    return iSum

